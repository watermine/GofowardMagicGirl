using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class MonsterGenetator : MonoBehaviour
{
    //ゴブリンのPrefab
    public GameObject GoblinPrefab;

    //時間計測用の変数
    private float delta = 0;

    //モンスターの生成間隔
    private float span = 1.0f;

    //モンスターの生成位置：X座標
    private float genPosX = 12;

    //キューブの生成位置オフセット：X座標
    private float offsetX = 0.5f;

    //モンスターの生成位置：Y座標
    private float genPosY = -3.12f;

    //モンスターの横方向の間隔
    private float spaceX = 1.4f;

    //モンスターの生成個数の上限
    private int maxBlocknum = 1;




    // Start is called before the first frame update
    void Start()
    {
        
    }

    // Update is called once per frame
    void Update()
    {
        this.delta += Time.deltaTime;

        //span以上の時間が経過したか調べる
        if(this.delta > this.span)
        {
            this.delta = 0;
            //生成するキューブ数をランダムに決める
            int n =Random.Range(1, maxBlocknum+1);

            //指定した数だけキューブを生成する
            for(int i = 0; i < n; i++)
            {
                //キューブの生成
                GameObject go = Instantiate(GoblinPrefab);
                go.transform.position = new Vector2(this.genPosX, this.genPosY);
            }
            //次のキューブまでの生成時間を決める
            this.span = this.offsetX + this.spaceX * n;
        }
    }
}
